const bcrypt = require('bcrypt');
const jwt = require('jsonwebtoken');
const pool = require('../config/db');
const asyncHandler = require('../middlewares/asyncHandler');
const { success, error } = require('../utils/response');
const generateOTP = require('../utils/generateOTP');
const sendEmail = require('../utils/sendEmail');

// POST /api/auth/register
const register = asyncHandler(async (req, res) => {
    const { first_name, last_name, email, phone, password, role, platform, device_type } = req.body;

    // Block Admin registration
    if (role === 'Admin') {
        return error(res, 403, 'Admin registration is not allowed');
    }

    // Only Doctor and Staff can register
    if (!['Doctor', 'Staff'].includes(role)) {
        return error(res, 400, 'Role must be Doctor or Staff');
    }

    // Check if email already exists
    const [existing] = await pool.query('SELECT id FROM users WHERE email = ?', [email]);
    if (existing.length > 0) {
        return error(res, 409, 'Email already registered');
    }

    // Hash password
    const hashedPassword = await bcrypt.hash(password, 10);

    // Insert user — user_code (DR0001/ST0001) auto-generated by trigger
    const [result] = await pool.query(
        `INSERT INTO users (first_name, last_name, email, phone, password, role, platform, device_type)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?)`,
        [first_name, last_name, email, phone || null, hashedPassword, role, platform || 'unknown', device_type || 'unknown']
    );

    // Fetch the created user with user_code
    const [user] = await pool.query(
        'SELECT id, user_code, first_name, last_name, email, role FROM users WHERE id = ?',
        [result.insertId]
    );

    return success(res, 201, 'Registration successful', user[0]);
});

// POST /api/auth/login
const login = asyncHandler(async (req, res) => {
    const { email, password, platform, device_type } = req.body;

    // Find user
    const [rows] = await pool.query(
        'SELECT id, first_name, last_name, email, password, role, user_code FROM users WHERE email = ? AND isDeleted = false',
        [email]
    );

    if (rows.length === 0) {
        return error(res, 401, 'Invalid email or password');
    }

    const user = rows[0];

    // Compare password
    const isMatch = await bcrypt.compare(password, user.password);
    if (!isMatch) {
        return error(res, 401, 'Invalid email or password');
    }

    // Generate tokens
    const accessToken = jwt.sign(
        { id: user.id, role: user.role },
        process.env.JWT_SECRET,
        { expiresIn: process.env.JWT_EXPIRES_IN }
    );

    const refreshToken = jwt.sign(
        { id: user.id },
        process.env.JWT_REFRESH_SECRET,
        { expiresIn: process.env.JWT_REFRESH_EXPIRES_IN }
    );

    // Save refresh token, platform, device_type, last_login
    await pool.query(
        'UPDATE users SET refresh_token = ?, platform = ?, device_type = ?, last_login_at = NOW() WHERE id = ?',
        [refreshToken, platform || 'unknown', device_type || 'unknown', user.id]
    );

    // Remove password from response
    delete user.password;

    return success(res, 200, 'Login successful', {
        user,
        accessToken,
        refreshToken,
    });
});

// POST /api/auth/forgot-password
const forgotPassword = asyncHandler(async (req, res) => {
    const { email } = req.body;

    // Check if user exists
    const [rows] = await pool.query(
        'SELECT id, first_name FROM users WHERE email = ? AND isDeleted = false',
        [email]
    );

    if (rows.length === 0) {
        return error(res, 404, 'No account found with this email');
    }

    // Generate OTP
    const otp = generateOTP(6);

    // Save OTP with 10 min expiry
    await pool.query(
        'UPDATE users SET otp = ?, otp_expiry = DATE_ADD(NOW(), INTERVAL 10 MINUTE) WHERE id = ?',
        [otp, rows[0].id]
    );

    // Send OTP via email
    await sendEmail(
        email,
        'Password Reset OTP',
        `<p>Hi ${rows[0].first_name},</p>
         <p>Your OTP for password reset is: <strong>${otp}</strong></p>
         <p>This OTP expires in 10 minutes.</p>`
    );

    return success(res, 200, 'OTP sent to your email');
});

// POST /api/auth/verify-otp
const verifyOTP = asyncHandler(async (req, res) => {
    const { email, otp } = req.body;

    const [rows] = await pool.query(
        'SELECT id FROM users WHERE email = ? AND otp = ? AND otp_expiry > NOW() AND isDeleted = false',
        [email, otp]
    );

    if (rows.length === 0) {
        return error(res, 400, 'Invalid or expired OTP');
    }

    return success(res, 200, 'OTP verified successfully');
});

// POST /api/auth/reset-password
const resetPassword = asyncHandler(async (req, res) => {
    const { email, otp, new_password } = req.body;

    // Verify OTP again
    const [rows] = await pool.query(
        'SELECT id FROM users WHERE email = ? AND otp = ? AND otp_expiry > NOW() AND isDeleted = false',
        [email, otp]
    );

    if (rows.length === 0) {
        return error(res, 400, 'Invalid or expired OTP');
    }

    // Hash new password
    const hashedPassword = await bcrypt.hash(new_password, 10);

    // Update password and clear OTP
    await pool.query(
        'UPDATE users SET password = ?, otp = NULL, otp_expiry = NULL WHERE id = ?',
        [hashedPassword, rows[0].id]
    );

    return success(res, 200, 'Password reset successful');
});

// GET /api/auth/me
const getMe = asyncHandler(async (req, res) => {
    const [rows] = await pool.query(
        `SELECT id, user_code, first_name, last_name, email, phone, role, platform, device_type, last_login_at, created_at
         FROM users WHERE id = ? AND isDeleted = false`,
        [req.user.id]
    );

    if (rows.length === 0) {
        return error(res, 404, 'User not found');
    }

    return success(res, 200, 'Profile fetched', rows[0]);
});

// POST /api/auth/logout
const logout = asyncHandler(async (req, res) => {
    await pool.query('UPDATE users SET refresh_token = NULL WHERE id = ?', [req.user.id]);
    return success(res, 200, 'Logged out successfully');
});

module.exports = { register, login, forgotPassword, verifyOTP, resetPassword, getMe, logout };
